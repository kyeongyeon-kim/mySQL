-- DATE (날짜표현)
-- TIME (시간표현)
-- DATETIME (두 가지 모두 표현) 문자열로 표현
-- TIMESTAMP (두 가지 모두 표현) 
SELECT CURRENT_DATE();
SELECT CURRENT_TIME();
SELECT CURRENT_TIMESTAMP();

SELECT DATE '2022-12-26';
SELECT DATE '2022/12/26';
