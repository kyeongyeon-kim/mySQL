-- SELECT DATE '2022-12-55';

SELECT TIME '12:48:33';

SELECT CAST('2022-12-26' AS DATE);
SELECT CONVERT('2022-12-26' , DATE);

SELECT '2022-12-26' + 1;
SELECT DATE '2022-12-26' + INTERVAL 7 DAY;
SELECT DATE '2022-12-26' - INTERVAL 7 DAY;
SELECT DATE '2022-12-26' + INTERVAL 7 WEEK;
SELECT DATE '2022-12-26' - INTERVAL 7 WEEK;
SELECT DATE '2022-12-26' + INTERVAL 7 YEAR;
SELECT DATE '2022-12-26' - INTERVAL 7 YEAR;

SELECT ADDDATE('2022-12-26', 1); -- 일 단위 연산
SELECT DATE_ADD('2022-12-26', INTERVAL 1 DAY); -- 원하는 단위 연산
